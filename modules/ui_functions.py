# GLOBALS
# ///////////////////////////////////////////////////////////////
GLOBAL_STATE = False
GLOBAL_TITLE_BAR = True


class UIFunctions(MainWindow):
    themes = Themes().items
    settings = Settings_Json().items

    def toggle_internet_connection_display(self):
        value = self.ui.continue_transcription.isHidden()
        self.ui.continue_transcription.setHidden(not value)
        self.ui.internet_connectivity_display.setHidden(not value)

    def set_current_date(self):
        date = QDate.currentDate()
        date = f"GLH-{date.toString('ddd d MMM, yyyy')}-"

        self.ui.text_unique_identifier.setText(date)
        self.ui.transcribe_date_label.setText(date)

    def add_transcribed_widgets(self):
        self.ui.start_transcription_button = PyPushButton(
            text="Start Transcription",
            radius=8,
            color=UIFunctions.themes["app_color"]["text_foreground"],
            bg_color=UIFunctions.themes["app_color"]["green"],
            bg_color_hover=UIFunctions.themes["app_color"]["dark_three"],
            bg_color_pressed=UIFunctions.themes["app_color"]["dark_four"]
        )
        self.ui.start_transcription_button.setFixedSize(QSize(200, 30))
        self.ui.start_transcription_button.setHidden(True)
        self.ui.horizontalLayout_15.insertWidget(0, self.ui.start_transcription_button)

    def add_editor_settings_widgets(self):
        self.ui.adjust_text_size = PySlider(
            bg_color=UIFunctions.themes["app_color"]["dark_three"],
            bg_color_hover=UIFunctions.themes["app_color"]["dark_three"],
            handle_color=UIFunctions.themes["app_color"]["context_color"],
            handle_color_hover=UIFunctions.themes["app_color"]["context_hover"],
            handle_color_pressed=UIFunctions.themes["app_color"]["context_pressed"]
        )
